package Day1;

public class Example2 
{
	public static void main(String[] args)
	{
		String name = "John";
		int age = 10;
		
		System.out.println(name+" "+age);
		
		System.out.println("Updating age");
		age = 27;
		System.out.println(name+" "+age);
		
		System.out.println("Updating Name");
		name = "David";
		System.out.println(name+" "+age);
	}
}
