package Day2.methods;

public class Example7 
{

	public static int add(int x, int y) 
	{
		int z = x+y;
		return z;
	}
	public static int add(int x, int y, int z) 
	{
		int result = x+y+z;
		return result;
	}
	
	public static double add(double x, int y, int z) 
	{
		double result = x+y+z;
		return result;
	}

}
