package Day2.methods;

public class Example3 
{
	
	
		public static int add(int x,int y)
	{
		int z = x+y;
		return z;
	}
		
		public static int sub(int x,int y)
		{

			int z = x-y;
			return z;
		}
		
		
	
		public static void main(String[] args) 
		{
			int res1 = add(4,5);
			int res2 = sub(14,8);
			
			System.out.println(res1);
			System.out.println(res2);
			
			
			
		}
	

}
